# Funci√≥n para mostrar estado del plan
show_plan_status() {
    if [ -f "PLAN.md" ]; then
        local completed=$(grep -c "\[x\]" PLAN.md 2>/dev/null || echo "0")
        local pending=$(grep -c "\[ \]" PLAN.md 2>/dev/null || echo "0")
        local total=$((completed + pending))

        if [ $total -gt 0 ]; then
            local percent=$((completed * 100 / total))
            echo -e "${CYAN}üìä Progreso: ${GREEN}$completed${NC}/${YELLOW}$total${NC} tareas (${percent}%)"

            # Barra de progreso
            local bar_width=30
            local filled=$((percent * bar_width / 100))
            local empty=$((bar_width - filled))

            echo -n "   ["
            printf "${GREEN}%${filled}s${NC}" | tr ' ' '‚ñà'
            printf "${YELLOW}%${empty}s${NC}" | tr ' ' '‚ñë'
            echo "]"
        fi
    fi
}

